import random

# –í–∞—Ä–∏–∞–Ω—Ç—ã –≤—ã–±–æ—Ä–∞
choices = ["–∫–∞–º–µ–Ω—å", "–Ω–æ–∂–Ω–∏—Ü—ã", "–±—É–º–∞–≥–∞"]

# –û—á–∫–∏ –∏–≥—Ä–æ–∫–æ–≤
player_score = 0
computer_score = 0

print("–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –∏–≥—Ä—É '–ö–∞–º–µ–Ω—å, –Ω–æ–∂–Ω–∏—Ü—ã, –±—É–º–∞–≥–∞'! –ò–≥—Ä–∞ –∏–¥–µ—Ç –¥–æ 3 –ø–æ–±–µ–¥.")

# –¶–∏–∫–ª –∏–≥—Ä—ã –¥–æ 3 –ø–æ–±–µ–¥
while player_score < 3 and computer_score < 3:
    # –ò–≥—Ä–æ–∫ –¥–µ–ª–∞–µ—Ç –≤—ã–±–æ—Ä
    player_choice = input("\n–í—ã–±–µ—Ä–∏—Ç–µ (–∫–∞–º–µ–Ω—å, –Ω–æ–∂–Ω–∏—Ü—ã, –±—É–º–∞–≥–∞): ").lower()

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –≤–≤–æ–¥–∞
    if player_choice not in choices:
        print("–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –≤–≤–æ–¥! –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.")
        continue

    # –ö–æ–º–ø—å—é—Ç–µ—Ä –¥–µ–ª–∞–µ—Ç —Å–ª—É—á–∞–π–Ω—ã–π –≤—ã–±–æ—Ä
    computer_choice = random.choice(choices)
    print(f"–ö–æ–º–ø—å—é—Ç–µ—Ä –≤—ã–±—Ä–∞–ª: {computer_choice}")

    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø–æ–±–µ–¥–∏—Ç–µ–ª—è —Ä–∞—É–Ω–¥–∞
    if player_choice == computer_choice:
        print("–ù–∏—á—å—è!")
    elif (player_choice == "–∫–∞–º–µ–Ω—å" and computer_choice == "–Ω–æ–∂–Ω–∏—Ü—ã") or \
         (player_choice == "–Ω–æ–∂–Ω–∏—Ü—ã" and computer_choice == "–±—É–º–∞–≥–∞") or \
         (player_choice == "–±—É–º–∞–≥–∞" and computer_choice == "–∫–∞–º–µ–Ω—å"):
        print("–í—ã –≤—ã–∏–≥—Ä–∞–ª–∏ —ç—Ç–æ—Ç —Ä–∞—É–Ω–¥!")
        player_score += 1
    else:
        print("–ö–æ–º–ø—å—é—Ç–µ—Ä –≤—ã–∏–≥—Ä–∞–ª —ç—Ç–æ—Ç —Ä–∞—É–Ω–¥!")
        computer_score += 1

    # –í—ã–≤–æ–¥–∏–º —Ç–µ–∫—É—â–∏–π —Å—á–µ—Ç
    print(f"–°—á–µ—Ç: –ò–≥—Ä–æ–∫ {player_score} - {computer_score} –ö–æ–º–ø—å—é—Ç–µ—Ä")

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø–æ–±–µ–¥–∏—Ç–µ–ª—è –∏–≥—Ä—ã
if player_score == 3:
    print("\nüéâ –ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã –≤—ã–∏–≥—Ä–∞–ª–∏ –∏–≥—Ä—É! üéâ")
else:
    print("\nüò¢ –ö–æ–º–ø—å—é—Ç–µ—Ä –ø–æ–±–µ–¥–∏–ª. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑! üò¢")
