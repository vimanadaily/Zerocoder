# –¢—ã —Ä–∞–∑—Ä–∞–±–∞—Ç—ã–≤–∞–µ—à—å –ø—Ä–æ–≥—Ä–∞–º–º–Ω–æ–µ –æ–±–µ—Å–ø–µ—á–µ–Ω–∏–µ –¥–ª—è —Å–µ—Ç–∏ –º–∞–≥–∞–∑–∏–Ω–æ–≤. –ö–∞–∂–¥—ã–π –º–∞–≥–∞–∑–∏–Ω –≤ —ç—Ç–æ–π —Å–µ—Ç–∏ –∏–º–µ–µ—Ç —Å–≤–æ–∏ –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–∏, –Ω–æ —Ç–∞–∫–∂–µ —Å—É—â–µ—Å—Ç–≤—É—é—Ç –æ–±—â–∏–µ —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏, —Ç–∞–∫–∏–µ –∫–∞–∫ –∞–¥—Ä–µ—Å, –Ω–∞–∑–≤–∞–Ω–∏–µ –∏ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç —Ç–æ–≤–∞—Ä–æ–≤. –í–∞—à–∞ –∑–∞–¥–∞—á–∞ ‚Äî —Å–æ–∑–¥–∞—Ç—å –∫–ª–∞—Å—Å Store, –∫–æ—Ç–æ—Ä—ã–π –º–æ–∂–Ω–æ –±—É–¥–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è —Ä–∞–∑–ª–∏—á–Ω—ã—Ö –º–∞–≥–∞–∑–∏–Ω–æ–≤.

class Store:
    """
    –ö–ª–∞—Å—Å Store –æ–ø–∏—Å—ã–≤–∞–µ—Ç –º–∞–≥–∞–∑–∏–Ω —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º, –∞–¥—Ä–µ—Å–æ–º –∏ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç–æ–º —Ç–æ–≤–∞—Ä–æ–≤.
    """

    def __init__(self, name: str, address: str):
        """
        –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä:
          - name    : –Ω–∞–∑–≤–∞–Ω–∏–µ –º–∞–≥–∞–∑–∏–Ω–∞ (—Å—Ç—Ä–æ–∫–∞)
          - address : –∞–¥—Ä–µ—Å –º–∞–≥–∞–∑–∏–Ω–∞ (—Å—Ç—Ä–æ–∫–∞)
          - items   : —Å–ª–æ–≤–∞—Ä—å {–Ω–∞–∑–≤–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞: —Ü–µ–Ω–∞}, –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –ø—É—Å—Ç–æ–π
        """
        self.name = name
        self.address = address
        self.items = {}  # –∑–¥–µ—Å—å –±—É–¥–µ–º —Ö—Ä–∞–Ω–∏—Ç—å —Ç–æ–≤–∞—Ä—ã –∏ –∏—Ö —Ü–µ–Ω—ã

    def add_item(self, item_name: str, price: float):
        """
        –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞ –≤ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç.
        –ï—Å–ª–∏ —Ç–æ–≤–∞—Ä —É–∂–µ –µ—Å—Ç—å ‚Äì –µ–≥–æ —Ü–µ–Ω–∞ –ø–µ—Ä–µ–∑–∞–ø–∏—à–µ—Ç—Å—è.
        """
        self.items[item_name] = price
        print(f"‚úÖ –¢–æ–≤–∞—Ä '{item_name}' –¥–æ–±–∞–≤–ª–µ–Ω/–æ–±–Ω–æ–≤–ª—ë–Ω —Å —Ü–µ–Ω–æ–π {price} ‚ÇΩ.")

    def remove_item(self, item_name: str):
        """
        –£–¥–∞–ª–µ–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞ –∏–∑ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç–∞.
        –ï—Å–ª–∏ —Ç–æ–≤–∞—Ä–∞ –Ω–µ—Ç ‚Äì —Å–æ–æ–±—â–∞–µ–º –æ–± —ç—Ç–æ–º.
        """
        if item_name in self.items:
            del self.items[item_name]
            print(f"üóë –¢–æ–≤–∞—Ä '{item_name}' —É–¥–∞–ª—ë–Ω –∏–∑ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç–∞.")
        else:
            print(f"‚ö† –¢–æ–≤–∞—Ä '{item_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω. –ù–µ—á–µ–≥–æ —É–¥–∞–ª—è—Ç—å.")

    def get_price(self, item_name: str):
        """
        –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ü–µ–Ω—É —Ç–æ–≤–∞—Ä–∞ –ø–æ –µ–≥–æ –Ω–∞–∑–≤–∞–Ω–∏—é.
        –ï—Å–ª–∏ —Ç–æ–≤–∞—Ä–∞ –Ω–µ—Ç ‚Äì –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç None.
        """
        return self.items.get(item_name)

    def update_price(self, item_name: str, new_price: float):
        """
        –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Ü–µ–Ω—ã —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Ç–æ–≤–∞—Ä–∞.
        –ï—Å–ª–∏ —Ç–æ–≤–∞—Ä–∞ –Ω–µ—Ç ‚Äì –≤—ã–≤–æ–¥–∏–º –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ.
        """
        if item_name in self.items:
            self.items[item_name] = new_price
            print(f"‚úè –¶–µ–Ω–∞ —Ç–æ–≤–∞—Ä–∞ '{item_name}' –æ–±–Ω–æ–≤–ª–µ–Ω–∞ –Ω–∞ {new_price} ‚ÇΩ.")
        else:
            print(f"‚ö† –¢–æ–≤–∞—Ä '{item_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω. –ù–µ–ª—å–∑—è –æ–±–Ω–æ–≤–∏—Ç—å —Ü–µ–Ω—É.")


# –ï—Å–ª–∏ –≤—ã —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç–µ —ç—Ç–æ—Ç —Ñ–∞–π–ª –∫–∞–∫ store.py, –Ω–∏–∂–µ –∫–æ–¥ –º–æ–∂–Ω–æ –ø–æ–º–µ—Å—Ç–∏—Ç—å –≤ main.py
if __name__ == "__main__":
    # –®–∞–≥¬†2: —Å–æ–∑–¥–∞—ë–º –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∞–≥–∞–∑–∏–Ω–æ–≤
    store1 = Store("–ì–∏–ø–µ—Ä–º–∞—Ä–∫–µ—Ç –ú–∞–≥–∏—è",    "—É–ª. –õ–µ–Ω–∏–Ω–∞, 10")
    store2 = Store("–ú–∏–Ω–∏–º–∞—Ä–∫–µ—Ç –£ –¥–æ–º–∞",   "–ø—Ä. –ú–∏—Ä–∞, 25")
    store3 = Store("–≠–∫–æ–ø—Ä–æ–¥—É–∫—Ç—ã",          "—É–ª. –ó–µ–ª—ë–Ω–∞—è, 5")

    # –î–æ–±–∞–≤–ª—è–µ–º —Ç–æ–≤–∞—Ä—ã –≤ –∫–∞–∂–¥—ã–π –º–∞–≥–∞–∑–∏–Ω
    store1.add_item("apples",  0.5)
    store1.add_item("bananas", 0.75)
    store1.add_item("milk",    1.2)

    store2.add_item("bread",   1.0)
    store2.add_item("eggs",    2.5)
    store2.add_item("water",   0.8)

    store3.add_item("organic apples",  1.5)
    store3.add_item("organic bananas", 1.8)
    store3.add_item("organic milk",    2.3)

    # –®–∞–≥¬†3: —Ç–µ—Å—Ç–∏—Ä—É–µ–º –º–µ—Ç–æ–¥—ã –Ω–∞ –æ–¥–Ω–æ–º –º–∞–≥–∞–∑–∏–Ω–µ (store1)
    print("\n=== –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –º–∞–≥–∞–∑–∏–Ω–∞:", store1.name, "===\n")

    # –ü–æ–ª—É—á–∞–µ–º —Ü–µ–Ω—É —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Ç–æ–≤–∞—Ä–∞
    price = store1.get_price("apples")
    print(f"–¶–µ–Ω–∞ 'apples': {price} ‚ÇΩ")  # –æ–∂–∏–¥–∞–µ–º 0.5

    # –ü—ã—Ç–∞–µ–º—Å—è –ø–æ–ª—É—á–∏—Ç—å —Ü–µ–Ω—É –Ω–µ—Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Ç–æ–≤–∞—Ä–∞
    missing = store1.get_price("chocolate")
    print(f"–¶–µ–Ω–∞ 'chocolate': {missing}")  # –æ–∂–∏–¥–∞–µ–º None

    # –û–±–Ω–æ–≤–ª—è–µ–º —Ü–µ–Ω—É —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Ç–æ–≤–∞—Ä–∞
    store1.update_price("bananas", 0.85)

    # –ü—ã—Ç–∞–µ–º—Å—è –æ–±–Ω–æ–≤–∏—Ç—å —Ü–µ–Ω—É –Ω–µ—Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ —Ç–æ–≤–∞—Ä–∞
    store1.update_price("chocolate", 1.5)

    # –£–¥–∞–ª—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π —Ç–æ–≤–∞—Ä
    store1.remove_item("milk")

    # –ü—ã—Ç–∞–µ–º—Å—è —É–¥–∞–ª–∏—Ç—å —Å–Ω–æ–≤–∞ —Ç–æ—Ç –∂–µ —Ç–æ–≤–∞—Ä
    store1.remove_item("milk")

    # –í—ã–≤–æ–¥–∏–º —Ç–µ–∫—É—â–µ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç–∞ store1
    print(f"\n–¢–µ–∫—É—â–∏–π –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç '{store1.name}':")
    for item, price in store1.items.items():
        print(f" - {item}: {price} ‚ÇΩ")
