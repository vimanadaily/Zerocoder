# Задание 1: Базовая обработка исключений
# напишите функцию safe_divide, которая принимает два аргумента: a и b. Функция должна пытаться делить a на b и
# возвращать результат. Если произойдет ошибка деления на ноль (ZeroDivisionError), функция должна возвращать None
# вместо возникновения исключения. Продемонстрируй работу функции на нескольких примерах, включая деление на ноль.

def safe_divide(a, b):
    try:
        return a / b  # Пытаемся выполнить деление
    except ZeroDivisionError:
        return None  # Возвращаем None, если деление на 0

# Тестирование функции с разными входными значениями
examples = [(10, 2), (7, 0), (6, 0), (0, 5)]

for a, b in examples:
    result = safe_divide(a, b)
    print(f"{a} / {b} = {result}")
